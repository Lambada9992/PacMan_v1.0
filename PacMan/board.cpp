void Board::clearPlayground()
{
    if(playground.size()!= 0){
        for(int i = 0;i < playground.size();i++){
            playground[i].clear();
            playground[i].resize(0);
        }
        playground.clear();
        playground.resize(0);
    }


}

void Board::resizePlayground(unsigned int y, unsigned int x)
{
    clearPlayground();
    this->playgroundSizeX = x;
    this->playgroundSizeY = y;

    playground.resize(this->playgroundSizeY);
    for(int i = 0; i < this->playgroundSizeY;i++){
        playground[i].resize(this->playgroundSizeX);
    }


}

