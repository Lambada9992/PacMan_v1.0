void Board::clearMap()
{
    if(map.size()!= 0){
        for(int i = 0;i < map.size();i++){
            map[i].clear();
            map[i].resize(0);
        }
        map.clear();
        map.resize(0);
    }

}

void Board::resizeMap(unsigned int y,unsigned int x)
{
    clearMap();
    this->mapSizeX = x;
    this->mapSizeY = y;

    map.resize(this->mapSizeY);
    for(int i = 0; i < this->mapSizeY;i++){
        map[i].resize(this->mapSizeX);
    }


}
